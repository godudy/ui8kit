// tagsgen emits utils/tags.go and utils/tags.ts from a single utils/tags.json
// source so the Go and TypeScript tag allow-lists cannot drift.
package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

type tagGroup struct {
	Name string   `json:"name"`
	Tags []string `json:"tags"`
}

type tagAlias struct {
	Name string   `json:"name"`
	Of   []string `json:"of"`
	Tags []string `json:"tags"`
}

type tagsFile struct {
	Groups  []tagGroup `json:"groups"`
	Aliases []tagAlias `json:"aliases"`
}

func main() {
	check := false
	if len(os.Args) > 1 && os.Args[1] == "--check" {
		check = true
	}
	root, err := findRepoRoot()
	if err != nil {
		fmt.Fprintf(os.Stderr, "tagsgen: %v\n", err)
		os.Exit(1)
	}
	in := filepath.Join(root, "utils", "tags.json")
	data, err := os.ReadFile(in)
	if err != nil {
		fmt.Fprintf(os.Stderr, "tagsgen: read %s: %v\n", in, err)
		os.Exit(1)
	}
	var tf tagsFile
	if err := json.Unmarshal(data, &tf); err != nil {
		fmt.Fprintf(os.Stderr, "tagsgen: parse %s: %v\n", in, err)
		os.Exit(1)
	}
	if err := validate(&tf); err != nil {
		fmt.Fprintf(os.Stderr, "tagsgen: %v\n", err)
		os.Exit(1)
	}

	targets := []struct {
		path    string
		content string
	}{
		{filepath.Join(root, "utils", "tags.go"), renderGo(&tf)},
		{filepath.Join(root, "utils", "tags.ts"), renderTS(&tf)},
	}
	var stale []string
	for _, t := range targets {
		existing, _ := os.ReadFile(t.path)
		if string(existing) == t.content {
			continue
		}
		if check {
			stale = append(stale, t.path)
			continue
		}
		if err := os.WriteFile(t.path, []byte(t.content), 0o644); err != nil {
			fmt.Fprintf(os.Stderr, "tagsgen: write %s: %v\n", t.path, err)
			os.Exit(1)
		}
		fmt.Printf("tagsgen: wrote %s\n", t.path)
	}
	if check && len(stale) > 0 {
		fmt.Fprintf(os.Stderr, "tagsgen: stale generated files (%d):\n", len(stale))
		for _, p := range stale {
			fmt.Fprintf(os.Stderr, "  %s\n", p)
		}
		os.Exit(1)
	}
}

func validate(tf *tagsFile) error {
	seen := map[string]bool{}
	for _, g := range tf.Groups {
		if seen[g.Name] {
			return fmt.Errorf("duplicate group %q", g.Name)
		}
		seen[g.Name] = true
	}
	for _, a := range tf.Aliases {
		if seen[a.Name] {
			return fmt.Errorf("duplicate group/alias %q", a.Name)
		}
		seen[a.Name] = true
		for _, ref := range a.Of {
			if !seen[ref] {
				return fmt.Errorf("alias %q references unknown group %q", a.Name, ref)
			}
		}
	}
	return nil
}

func findRepoRoot() (string, error) {
	cwd, err := os.Getwd()
	if err != nil {
		return "", err
	}
	dir := cwd
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("go.mod not found from %s", cwd)
		}
		dir = parent
	}
}

// renderGo emits utils/tags.go.
func renderGo(tf *tagsFile) string {
	var b bytes.Buffer
	b.WriteString("// Code generated by tagsgen. DO NOT EDIT.\n\n")
	b.WriteString("// TagGroup restricts HTML tags for one primitive semantic group.\n")
	b.WriteString("// Source of truth: utils/tags.json.\n")
	b.WriteString("package uiutils\n\n")
	b.WriteString("import \"strings\"\n\n")
	b.WriteString("// TagGroup restricts which HTML tags a primitive may render.\n")
	b.WriteString("type TagGroup int\n\n")
	b.WriteString("const (\n")
	for i, g := range tf.Groups {
		if i == 0 {
			fmt.Fprintf(&b, "\tTagGroup%s TagGroup = iota\n", g.Name)
		} else {
			fmt.Fprintf(&b, "\tTagGroup%s\n", g.Name)
		}
	}
	for _, a := range tf.Aliases {
		fmt.Fprintf(&b, "\tTagGroup%s\n", a.Name)
	}
	b.WriteString(")\n\n")
	b.WriteString("// ResolveTag returns tag when allowed for group, otherwise fallback.\n")
	b.WriteString("func ResolveTag(tag, fallback string, group TagGroup) string {\n")
	b.WriteString("\tt := strings.TrimSpace(strings.ToLower(tag))\n")
	b.WriteString("\tif t == \"\" {\n\t\treturn fallback\n\t}\n")
	b.WriteString("\tif IsAllowedTag(t, group) {\n\t\treturn t\n\t}\n")
	b.WriteString("\treturn fallback\n}\n\n")
	b.WriteString("// IsAllowedTag reports whether tag is valid for the semantic group.\n")
	b.WriteString("func IsAllowedTag(tag string, group TagGroup) bool {\n")
	b.WriteString("\tt := strings.TrimSpace(strings.ToLower(tag))\n")
	b.WriteString("\tswitch group {\n")
	for _, g := range tf.Groups {
		fmt.Fprintf(&b, "\tcase TagGroup%s:\n\t\treturn oneOf(t, %s)\n", g.Name, goOneOfArgs(g.Tags))
	}
	for _, a := range tf.Aliases {
		fmt.Fprintf(&b, "\tcase TagGroup%s:\n\t\treturn %s\n", a.Name, goAliasExpr(a))
	}
	b.WriteString("\tdefault:\n\t\treturn false\n\t}\n}\n\n")
	b.WriteString("func oneOf(value string, options ...string) bool {\n")
	b.WriteString("\tfor _, opt := range options {\n\t\tif value == opt {\n\t\t\treturn true\n\t\t}\n\t}\n")
	b.WriteString("\treturn false\n}\n")
	return b.String()
}

func goAliasExpr(a tagAlias) string {
	var parts []string
	for _, ref := range a.Of {
		parts = append(parts, fmt.Sprintf("IsAllowedTag(t, TagGroup%s)", ref))
	}
	if len(a.Tags) > 0 {
		parts = append(parts, fmt.Sprintf("oneOf(t, %s)", goOneOfArgs(a.Tags)))
	}
	if len(parts) == 0 {
		return "false"
	}
	return strings.Join(parts, " || ")
}

func aliasExtra(tags []string, first bool) string {
	if len(tags) == 0 {
		return ""
	}
	var b strings.Builder
	if first {
		b.WriteString(" || oneOf(t, ")
	} else {
		b.WriteString(" || oneOf(t, ")
	}
	b.WriteString(goOneOfArgs(tags))
	b.WriteString(")")
	return b.String()
}

func goOneOfArgs(tags []string) string {
	quoted := make([]string, len(tags))
	for i, t := range tags {
		quoted[i] = fmt.Sprintf("%q", t)
	}
	return strings.Join(quoted, ", ")
}

// renderTS emits utils/tags.ts.
func renderTS(tf *tagsFile) string {
	var b bytes.Buffer
	b.WriteString("// Code generated by tagsgen. DO NOT EDIT.\n\n")
	b.WriteString("export const TagGroup = {\n")
	for _, g := range tf.Groups {
		fmt.Fprintf(&b, "  %s: \"%s\",\n", g.Name, g.Name)
	}
	for _, a := range tf.Aliases {
		fmt.Fprintf(&b, "  %s: \"%s\",\n", a.Name, a.Name)
	}
	b.WriteString("} as const;\n\n")
	b.WriteString("export type TagGroup = (typeof TagGroup)[keyof typeof TagGroup];\n\n")
	for _, g := range tf.Groups {
		fmt.Fprintf(&b, "const %sTags = new Set(%s);\n", lowerFirst(g.Name), tsArray(g.Tags))
	}
	for _, a := range tf.Aliases {
		if len(a.Tags) == 0 {
			continue
		}
		fmt.Fprintf(&b, "const %sTags = new Set(%s);\n", lowerFirst(a.Name), tsArray(a.Tags))
	}
	b.WriteString("\nfunction oneOf(tag: string, options: Set<string>): boolean {\n")
	b.WriteString("  return options.has(tag);\n}\n\n")
	b.WriteString("export function isAllowedTag(tag: string, group: TagGroup): boolean {\n")
	b.WriteString("  const t = tag.trim().toLowerCase();\n")
	b.WriteString("  switch (group) {\n")
	for _, g := range tf.Groups {
		fmt.Fprintf(&b, "    case TagGroup.%s:\n      return oneOf(t, %sTags);\n", g.Name, lowerFirst(g.Name))
	}
	for _, a := range tf.Aliases {
		fmt.Fprintf(&b, "    case TagGroup.%s:\n      return %s;\n", a.Name, tsAliasExpr(a))
	}
	b.WriteString("    default:\n      return false;\n  }\n}\n\n")
	b.WriteString("export function resolveTag(tag: string | undefined, fallback: string, group: TagGroup): string {\n")
	b.WriteString("  const t = tag?.trim().toLowerCase() ?? \"\";\n")
	b.WriteString("  if (!t) return fallback;\n")
	b.WriteString("  return isAllowedTag(t, group) ? t : fallback;\n}\n")
	return b.String()
}

func tsAliasExpr(a tagAlias) string {
	var parts []string
	for _, ref := range a.Of {
		parts = append(parts, fmt.Sprintf("%sTags.has(t)", lowerFirst(ref)))
	}
	if len(a.Tags) > 0 {
		parts = append(parts, fmt.Sprintf("oneOf(t, %sTags)", lowerFirst(a.Name)))
	}
	if len(parts) == 0 {
		return "false"
	}
	return strings.Join(parts, " || ")
}

func tsArray(tags []string) string {
	quoted := make([]string, len(tags))
	for i, t := range tags {
		quoted[i] = fmt.Sprintf("%q", t)
	}
	return "[" + strings.Join(quoted, ", ") + "]"
}

func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

var _ = sort.Strings
