package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

func main() {
	check := false
	if len(os.Args) > 1 && os.Args[1] == "--check" {
		check = true
	}
	repoRoot, err := findRepoRoot()
	if err != nil {
		fmt.Fprintf(os.Stderr, "variantgen: %v\n", err)
		os.Exit(1)
	}
	dirs, err := discoverVariantDirs(repoRoot)
	if err != nil {
		fmt.Fprintf(os.Stderr, "variantgen: %v\n", err)
		os.Exit(1)
	}
	if len(dirs) == 0 {
		fmt.Println("variantgen: no *.variants.json found under ui/ or components/")
		return
	}
	var stale []string
	for _, dir := range dirs {
		outPath, content, err := generateBrickGen(dir)
		if err != nil {
			fmt.Fprintf(os.Stderr, "variantgen: %s: %v\n", dir, err)
			os.Exit(1)
		}
		if content == "" {
			continue
		}
		existing, readErr := os.ReadFile(outPath)
		if readErr != nil && !os.IsNotExist(readErr) {
			fmt.Fprintf(os.Stderr, "variantgen: read %s: %v\n", outPath, readErr)
			os.Exit(1)
		}
		if string(existing) == content {
			continue
		}
		if check {
			stale = append(stale, outPath)
			continue
		}
		if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
			fmt.Fprintf(os.Stderr, "variantgen: write %s: %v\n", outPath, err)
			os.Exit(1)
		}
		fmt.Printf("variantgen: wrote %s\n", outPath)
	}
	if check && len(stale) > 0 {
		fmt.Fprintf(os.Stderr, "variantgen: stale generated files (%d):\n", len(stale))
		for _, p := range stale {
			fmt.Fprintf(os.Stderr, "  %s\n", p)
		}
		fmt.Fprintf(os.Stderr, "run: go run ./.validate/cmd/variantgen\n")
		os.Exit(1)
	}
	if !check {
		fmt.Printf("variantgen: OK (%d dirs)\n", len(dirs))
	}
}

func findRepoRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			if _, err := os.Stat(filepath.Join(dir, "ui")); err == nil {
				return dir, nil
			}
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("repo root not found")
		}
		dir = parent
	}
}

func discoverVariantDirs(repoRoot string) ([]string, error) {
	var dirs []string
	for _, rootName := range []string{"ui", "components"} {
		root := filepath.Join(repoRoot, rootName)
		err := filepath.WalkDir(root, func(path string, d os.DirEntry, err error) error {
			if err != nil {
				return err
			}
			if d.IsDir() {
				return nil
			}
			if !strings.HasSuffix(path, ".variants.json") {
				return nil
			}
			dir := filepath.Dir(path)
			dirs = append(dirs, dir)
			return nil
		})
		if err != nil && !os.IsNotExist(err) {
			return nil, err
		}
	}
	// unique dirs
	seen := map[string]bool{}
	var out []string
	for _, d := range dirs {
		if !seen[d] {
			seen[d] = true
			out = append(out, d)
		}
	}
	sort.Strings(out)
	return out, nil
}

func generateBrickGen(dir string) (outPath string, content string, err error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return "", "", err
	}
	var files []string
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		name := e.Name()
		if strings.HasSuffix(name, ".variants.json") {
			files = append(files, name)
		}
	}
	if len(files) == 0 {
		return "", "", nil
	}
	pkg := packageName(dir)
	outPath = filepath.Join(dir, "brick_gen.go")

	var b strings.Builder
	b.WriteString("// Code generated by variantgen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	b.WriteString("import (\n")
	b.WriteString("\t_ \"embed\"\n\n")
	b.WriteString("\t\"github.com/fastygo/templ/utils\"\n")
	b.WriteString(")\n\n")

	for _, file := range files {
		varName := variantVarName(file)
		embedVar := camelLower(varName) + "JSON"
		fmt.Fprintf(&b, "//go:embed %s\n", file)
		fmt.Fprintf(&b, "var %s []byte\n\n", embedVar)
		fmt.Fprintf(&b, "var %s = uiutils.MustParseVariantRecipe(%s).ToVariants()\n\n", varName, embedVar)
	}

	return outPath, b.String(), nil
}

func variantVarName(file string) string {
	base := strings.TrimSuffix(file, ".variants.json")
	if base == "" {
		return "Variants"
	}
	parts := strings.Split(base, "-")
	for i, p := range parts {
		if len(p) == 0 {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + p[1:]
	}
	return strings.Join(parts, "") + "Variants"
}

func camelLower(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

func packageName(dir string) string {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return filepath.Base(dir)
	}
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".templ") {
			continue
		}
		data, err := os.ReadFile(filepath.Join(dir, e.Name()))
		if err != nil {
			continue
		}
		for _, line := range strings.Split(string(data), "\n") {
			line = strings.TrimSpace(line)
			if strings.HasPrefix(line, "package ") {
				return strings.TrimSpace(strings.TrimPrefix(line, "package "))
			}
		}
	}
	return filepath.Base(dir)
}
