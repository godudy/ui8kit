package main

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestGenerateBlockOutput(t *testing.T) {
	repoRoot, err := findRepoRoot()
	if err != nil {
		t.Fatal(err)
	}
	specPath := filepath.Join(repoRoot, "examples", "templ", "ui", "blocks", "home", "home.spec.md")
	doc, err := loadSpec(specPath)
	if err != nil {
		t.Fatal(err)
	}
	spec := blockSpec{
		path: specPath,
		dir:  filepath.Dir(specPath),
		doc:  *doc,
	}
	outPath, content, err := generateBlock(spec)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.HasSuffix(outPath, "page_gen.go") {
		t.Fatalf("unexpected output path: %s", outPath)
	}
	for _, want := range []string{
		"// Code generated by blockgen. DO NOT EDIT.",
		"func DefaultPage() PageProps",
		"func BlockVariant(key, value string) string",
		`"github.com/fastygo/templ/examples/data"`,
		"data.HomeDataJSON",
		"//go:embed home.variants.json",
	} {
		if !strings.Contains(content, want) {
			t.Fatalf("generated content missing %q", want)
		}
	}
	if strings.Contains(content, "//go:embed home.data.json") {
		t.Fatalf("data-package mode should not emit local //go:embed for data JSON")
	}
}

func TestBlockgenCheckFresh(t *testing.T) {
	repoRoot, err := findRepoRoot()
	if err != nil {
		t.Fatal(err)
	}
	specs, err := discoverBlockSpecs(repoRoot)
	if err != nil {
		t.Fatal(err)
	}
	for _, spec := range specs {
		outPath, content, err := generateBlock(spec)
		if err != nil {
			t.Fatal(err)
		}
		existing, err := os.ReadFile(outPath)
		if err != nil {
			t.Fatalf("missing generated file %s: %v", outPath, err)
		}
		if string(existing) != content {
			t.Fatalf("stale generated file %s; run blockgen", outPath)
		}
	}
}
