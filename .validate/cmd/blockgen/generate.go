package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

func generateBlock(spec blockSpec) (outPath string, content string, err error) {
	cfg := resolveCodegen(spec)
	dataFile := stringField(spec.doc.fm, "data")
	variantsFile := stringField(spec.doc.fm, "variants")
	dataPackage := stringField(spec.doc.fm, "data-package")
	dataVar := stringField(spec.doc.fm, "data-var")
	dataShared := dataPackage != "" && dataVar != ""
	variantsPackage := stringField(spec.doc.fm, "variants-package")
	variantsVar := stringField(spec.doc.fm, "variants-var")
	variantsShared := variantsPackage != "" && variantsVar != ""
	if dataFile == "" && variantsFile == "" && !dataShared && !variantsShared {
		return "", "", nil
	}

	pkg := packageName(spec)
	outPath = filepath.Join(spec.dir, cfg.Output)

	var b strings.Builder
	b.WriteString("// Code generated by blockgen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)

	// Determine imports up front. Local //go:embed is only needed for files
	// that are colocated with the block (i.e. not resolved via a
	// `*-package`/`*-var` cross-package reference). Shared data/variants are
	// loaded from another Go package via a normal import — the same package
	// may serve both data and variants for a given block, so dedupe imports.
	needsEmbed := (variantsFile != "" && !variantsShared) || (dataFile != "" && !dataShared)
	needsJSON := dataFile != "" || variantsFile != "" || dataShared || variantsShared
	sharedPackages := map[string]bool{}
	if dataShared {
		sharedPackages[dataPackage] = true
	}
	if variantsShared {
		sharedPackages[variantsPackage] = true
	}
	if needsEmbed || needsJSON {
		b.WriteString("import (\n")
		if needsEmbed {
			b.WriteString("\t_ \"embed\"\n")
		}
		if needsJSON {
			b.WriteString("\t\"encoding/json\"\n")
		}
		if len(sharedPackages) > 0 {
			b.WriteString("\n")
			for _, imp := range sortedStringKeys(sharedPackages) {
				fmt.Fprintf(&b, "\t%q\n", imp)
			}
		}
		b.WriteString(")\n\n")
	}

	// Data source: local embed OR cross-package byte slice.
	if dataFile != "" || dataShared {
		if dataFile != "" {
			if err := ensureFileExists(spec.dir, dataFile); err != nil {
				return "", "", err
			}
		}
		if !dataShared {
			fmt.Fprintf(&b, "//go:embed %s\n", dataFile)
			b.WriteString("var blockDataJSON []byte\n\n")
		}
		fmt.Fprintf(&b, "type showcaseDoc struct {\n\tShowcase map[string]struct {\n\t\tProps %s `json:\"props\"`\n\t} `json:\"showcase\"`\n}\n\n", cfg.Data.PropsType)
	}

	// Variants source: local embed OR cross-package byte slice.
	if variantsFile != "" || variantsShared {
		if variantsFile != "" && !variantsShared {
			if err := ensureFileExists(spec.dir, variantsFile); err != nil {
				return "", "", err
			}
			fmt.Fprintf(&b, "//go:embed %s\n", variantsFile)
			b.WriteString("var blockVariantsJSON []byte\n\n")
		}
		b.WriteString("type blockVariantRecipe struct {\n")
		b.WriteString("\tBase     string                       `json:\"base\"`\n")
		b.WriteString("\tKeys     []string                     `json:\"keys\"`\n")
		b.WriteString("\tByKey    map[string]map[string]string `json:\"byKey\"`\n")
		b.WriteString("\tDefaults map[string]string            `json:\"defaults\"`\n")
		b.WriteString("}\n\n")
		b.WriteString("var blockRecipe blockVariantRecipe\n\n")
		variantsSrc := "blockVariantsJSON"
		variantsSource := variantsFile
		if variantsShared {
			variantsSrc = packageBase(variantsPackage) + "." + variantsVar
			variantsSource = variantsPackage + "." + variantsVar
		}
		fmt.Fprintf(&b, "func init() {\n\tif err := json.Unmarshal(%s, &blockRecipe); err != nil {\n\t\tpanic(\"%s: %s: \" + err.Error())\n\t}\n}\n\n", variantsSrc, pkg, variantsSource)
	}

	if dataFile != "" || dataShared {
		source := dataFile
		if dataShared {
			source = dataPackage + "." + dataVar
		}
		fmt.Fprintf(&b, "// %s returns demo data from %s.\n", cfg.Data.DefaultFunction, source)
		fmt.Fprintf(&b, "func %s() %s {\n", cfg.Data.DefaultFunction, cfg.Data.PropsType)
		fmt.Fprintf(&b, "\tvar doc showcaseDoc\n")
		src := "blockDataJSON"
		if dataShared {
			src = packageBase(dataPackage) + "." + dataVar
		}
		fmt.Fprintf(&b, "\tif err := json.Unmarshal(%s, &doc); err != nil {\n", src)
		fmt.Fprintf(&b, "\t\tpanic(\"%s: %s: \" + err.Error())\n\t}\n", pkg, source)
		fmt.Fprintf(&b, "\tentry, ok := doc.Showcase[%q]\n", cfg.Data.Ref)
		fmt.Fprintf(&b, "\tif !ok {\n")
		fmt.Fprintf(&b, "\t\tpanic(\"%s: %s: missing showcase.%s\")\n\t}\n", pkg, source, cfg.Data.Ref)
		b.WriteString("\treturn entry.Props\n")
		b.WriteString("}\n\n")
	}

	if variantsFile != "" || variantsShared {
		source := variantsFile
		if variantsShared {
			source = variantsPackage + "." + variantsVar
		}
		fmt.Fprintf(&b, "// BlockVariant resolves a block-level visual preset from %s.\n", source)
		b.WriteString("func BlockVariant(key, value string) string {\n")
		b.WriteString("\tchoices, ok := blockRecipe.ByKey[key]\n")
		b.WriteString("\tif !ok {\n")
		b.WriteString("\t\treturn \"\"\n")
		b.WriteString("\t}\n")
		b.WriteString("\tif variant, ok := choices[value]; ok {\n")
		b.WriteString("\t\treturn variant\n")
		b.WriteString("\t}\n")
		b.WriteString("\treturn blockRecipe.Defaults[key]\n")
		b.WriteString("}\n")
	}

	return outPath, b.String(), nil
}

func ensureFileExists(dir, name string) error {
	path := filepath.Join(dir, name)
	if _, err := os.Stat(path); err != nil {
		return fmt.Errorf("missing referenced file %q", name)
	}
	return nil
}

// packageBase extracts the short package alias (last path segment) from a
// full Go import path so blockgen-generated code can reference exported
// symbols like `data.HomeDataJSON`.
func packageBase(importPath string) string {
	slash := strings.LastIndex(importPath, "/")
	if slash < 0 {
		return importPath
	}
	return importPath[slash+1:]
}

func sortedStringKeys(m map[string]bool) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}
